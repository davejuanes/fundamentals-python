# Operadores aritméticos

x = 5
y = 10

# Suma
print("Suma", x + y)

# Resta
print("Resta", x - y)

# Multiplicación
print("Multiplicación", x * y)

# División (Siempre retorna un float)
print("División", y / x)

# Módulo %
print("Módulo(resto)", y % x)

# Potencia **
print("Pontencia", y ** x)

# División entera //
print("División Entera", y // x) # No da valores decimales

par = 8
print(par % 2 == 0)

